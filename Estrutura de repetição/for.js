// sintaxe = escrita do código

// for - laço de repetição = loop
// um laço de repetição tem voltas 

//laço contado - quando sabemos quantas voltas
//laço condicional - irá fazer as voltas se a condição for verdadeira

// a variavel i vale 0 ou seja essa variável armazena as voltas
// a condição verifica o número que o loop está
for (let i = 0; i <= 9; i = i + 1) {
    console.log("A volta que o for está é: " + i)
}

//utilize o for para exibir do 1 ao 10
for (let i = 1; i <= 10; i++) {
    console.log(i)
}